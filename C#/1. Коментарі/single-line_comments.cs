using System;

namespace SingleLineComments
{
    class Program
    {
        static void Main()
        {
            // Секція: Однорядкові коментарі в C#
            // У C# однорядкові коментарі — це текстові пояснення в коді, які позначаються символами // і охоплюють решту поточного рядка.
            // Коментарі є частиною вихідного коду, але ігноруються компілятором і не впливають на виконання програми.
            // Використовуються для документування коду, пояснення логіки, тимчасового виключення коду або додавання приміток для розробників.
            // Однорядкові коментарі обмежені одним рядком; для багаторядкових пояснень використовуються багаторядкові коментарі (/* */).
            // Розмір у пам’яті: коментарі не займають пам’ять у скомпільованій програмі, оскільки вони виключаються компілятором.

            // Однорядковий коментар: Позначається символами // і охоплює текст до кінця поточного рядка.
            // Застосування: документування коду, пояснення змінних, методів, алгоритмів або тимчасове відключення коду.
            // Перевага: простий і швидкий спосіб додавання пояснень, підвищує читабельність коду.
            // Обмеження: обмежується одним рядком; для довгих пояснень потрібні багаторядкові коментарі або XML-коментарі (///).
            int number = 42; // Приклад: ініціалізація змінної number зі значенням 42.
                             // Цей коментар пояснює призначення змінної.
                             // Помилка: int x = 10; // int y = 20; — не компілюється, оскільки код після // ігнорується.

            // Приклади використання:
            double price = 99.99; // Встановлення ціни товару в доларах.
            // string debug = "Test"; // Цей рядок закоментований і не виконується.
            char letter = 'A'; // Позначення символу для подальшої обробки.

            // Помилки:
            // int x = 10; y = 20; // Друга частина (y = 20) ігнорується компілятором, що призведе до помилки, якщо y не оголошено.
            // // int z = 30; — весь рядок ігнорується, навіть якщо він містить синтаксично правильний код.
        }
    }
}
// Додаткові зауваження:
// 1. Синтаксис: Однорядковий коментар починається з //, і все після нього в поточному рядку ігнорується компілятором.
//    Наприклад: int x = 5; // Це коментар — компілятор бачить лише int x = 5;.
// 2. Використання для дебагінгу: Однорядкові коментарі часто використовуються для тимчасового виключення коду під час тестування.
//    Наприклад: // Console.WriteLine("Debug"); — відключить виведення.
// 3. Читабельність: Використовуйте однорядкові коментарі для коротких пояснень; для детальних описів методів або класів краще використовувати XML-коментарі (///).
//    Наприклад: /// <summary>Опис методу</summary> для документації.
// 4. Практичне використання: Додавайте однорядкові коментарі для пояснення складної логіки, позначення змінних
//    або тимчасового відключення коду під час розробки чи дебагінгу.
// 5. Обмеження: Коментарі не повинні замінювати зрозумілий код; надмірне використання може погіршити читабельність.
//    Наприклад, замість // Додаємо 1 до x краще написати зрозумілу назву змінної, наприклад, increment.
